package JavaChallenges;
public class OperatorsPracticeProblems{
    public static void main(String [] args){
        // Write a program to print the sum and product of two numbers using arithmetic operators.

        int num1 = 12;
        int num2 = 23;

        // Arithmetic operators 

        System.out.println("Sum = "+num1+num2);
        System.out.println("Product = "+num1*num2);

        // Write a program to check whether a number is even or odd using the modulus operator.

        int num = 10;   // Assign the value 

        if (num%2==0){      // If block 
            System.out.println("Even");
        }
        else{   //Else block 
            System.out.println("Odd");
        }


        
        
    }
}