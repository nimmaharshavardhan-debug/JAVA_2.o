package Operators;

public class AssignmentOperators {
    public static void main(String [] args){
        // intializing the values 


        int num = 3;

        // num = num + 2;
        num +=2;
        System.out.println("After +=2 = "+num);

        // num = num - 1;
        num -=1;
        System.out.println("After -=1 = "+num);

        // num = num*2;
        num *= 2;
        System.out.println("After *=2 = "+num);

        // num = num/1;
        num /=1;
        System.out.println("After /=1 = "+num);

        // num = num%3;
        num %=3;
        System.out.println("After %=3 = "+num);


    }
    
}
