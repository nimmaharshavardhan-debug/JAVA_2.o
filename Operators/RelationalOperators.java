package Operators;

public class RelationalOperators {
    public static void main(String [] args){
        // ralational operators 

        // intializing the values 

        int a = 30;
        int b = 23;

        // display the values 

        System.out.println("> operator = "+(a>b));
        System.out.println("< operator = "+(a<b));
        System.out.println(">= operator = "+(a>=b));
        System.out.println("<= operator = "+(a<=b));
        System.out.println("== operator = "+(a==b));
        System.out.println("!= operator = "+(a!=b));
    }
    
}
